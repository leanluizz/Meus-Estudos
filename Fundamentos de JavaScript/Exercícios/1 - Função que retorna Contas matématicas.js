function Soma(a, b) {
    return a + b
}
function Subtração(a, b) {
    return a - b
}
function Multiplicacao(a, b) {
    return a * b
}
function Divisao(a, b) {
    return a / b
}
function Contas(a, b) {
    return eval(a, b)
}

