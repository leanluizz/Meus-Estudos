/*Função de potencia retorna base multiplicada AS MESMAS VEZES de acordo com o valor do expoente
 Ex: Potenciacao(4, 3) = 4 x 4 x 4 = 64 

 Math.pow(base, expoente) Objeto Math que resolve Potencia*/

 function Potenciacao(base, expoente) {
    return Math.pow(base, expoente);
}
console.log(Potenciacao(4, 3));